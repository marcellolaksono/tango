package students;

/**
 * A simple enumeration corresponding to the University of Glasgow 22-point grading scale.
 * Use .ordinal() to access the number of points for a Grade instance.
 * 
 * @author mefoster
 */
public enum Grade {
	H, 
	G2, G1, 
	F3, F2, F1, 
	E3, E2, E1, 
	D3, D2, D1, 
	C3, C2, C1, 
	B3, B2, B1, 
	A5, A4, A3, A2, A1;
}
